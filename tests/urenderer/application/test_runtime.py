class DummyRenderer(Renderer):
    def start(self, camera: Camera, view_matrix: ndarray, name: str) -> None:
        self.node_transformations = {}

    def validate(self, node: Node) -> bool:
        return True

    def render_valid_node(self, node: Node, model_transformation: ndarray) -> None:
        self.node_transformations[node.name] = model_transformation

    def end(self, capture: bool = False) -> None:
        pass


